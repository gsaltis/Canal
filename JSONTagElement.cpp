/*****************************************************************************!
 * Global Headers
 *****************************************************************************/
#include <QtCore>
#include <QtGui>
#include <QWidget>

/*****************************************************************************!
 * Local Headers
 *****************************************************************************/
#include "JSONTagElement.h"

/*****************************************************************************!
 * Function : JSONTagElement
 *****************************************************************************/
JSONTagElement::JSONTagElement
(
 QString                                InTag,
 QJsonValue::Type                       InType
) : QWidget()
{
  type = InType;
  tag = InTag;
}

/*****************************************************************************!
 * Function : ~JSONTagElement
 *****************************************************************************/
JSONTagElement::~JSONTagElement
()
{
}

/*****************************************************************************!
 * Function : Equals
 *****************************************************************************/
bool
JSONTagElement::Equals
(QString InTag, QJsonValue::Type InType)
{
  if ( tag == InTag && type == InType ) {
    return true;
  }
  return false;
}

/*****************************************************************************!
 * Function : GetTag 
 *****************************************************************************/
QString
JSONTagElement::GetTag
()
{
  return tag;
}

/*****************************************************************************!
 * Function : GetType
 *****************************************************************************/
QJsonValue::Type
JSONTagElement::GetType
()
{
  return type;
}
